type CP437Full struct{}

var _ encoding.Encoding = CP437Full{}

func (c CP437Full) NewDecoder() *encoding.Decoder {
	return &encoding.Decoder{Transformer: CP437FullDecodeTransformer{}}
}

func (c CP437Full) NewEncoder() *encoding.Encoder {
	return &encoding.Encoder{Transformer: CP437FullEncodeTransformer{}}
}

type CP437FullDecodeTransformer struct {
	transform.NopResetter
}

func (t CP437FullDecodeTransformer) Transform(dst, src []byte, atEOF bool) (nDst, nSrc int, err error) {
	for i, c := range src {
		decode := cp437Decode[c]
		n := int(decode.len)
		if nDst+n > len(dst) {
			err = transform.ErrShortDst
			break
		}

		for j := 0; j < n; j++ {
			dst[nDst] = decode.data[j]
			nDst++
		}
		nSrc = i + 1
	}

	return nDst, nSrc, err
}

type CP437FullEncodeTransformer struct {
	transform.NopResetter
}

func (t CP437FullEncodeTransformer) Transform(dst, src []byte, atEOF bool) (nDst, nSrc int, err error) {
	r, size := rune(0), 0
loop:
	for nSrc < len(src) {
		if nDst >= len(dst) {
			err = transform.ErrShortDst
			break
		}
		r = rune(src[nSrc])

		if r < utf8.RuneSelf {
			nSrc++
			dst[nDst] = uint8(r)
			nDst++
			continue
		}

		r, size = utf8.DecodeRune(src[nSrc:])
		if size == 1 {
			if !atEOF && !utf8.FullRune(src[nSrc:]) {
				err = transform.ErrShortSrc
			} else {
				err = RepertoireError(0x1a)
			}
			break
		}

		for low, high := 0, 0x100; ; {
			if low >= high {
				err = RepertoireError(0x1a)
				break loop
			}
			mid := (low + high) / 2
			got := cp437Encode[mid]
			gotRune := rune(got & (1<<24 - 1))
			if gotRune < r {
				low = mid + 1
			} else if gotRune > r {
				high = mid
			} else {
				dst[nDst] = byte(got >> 24)
				nDst++
				break
			}
		}
		nSrc += size
	}

	return nDst, nSrc, err
}
